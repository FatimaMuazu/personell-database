<p>Section 2(3) of the Act, provides that  a public institution shall cause to be published in accordance with 
subsection(4) of this section, the following information
<ol type="a"><li>A description of the organization and responsibilities of the institution including details of the programmes 
and functions of each division, branch and department of the institution.</li> 
	<li>A list of all- 
				<ol type="i"><li>Classes of records under the control of the institution in sufficient details to 
					facilitate the exercise of the right to information under this Act.</li> 
				<li>Manuals used by employees of the institution in administerig or carrying out any of the programmes or activities of the institution.</li>
				</ol></li> 
	<li>A description of documents containing final opinions including concurring and dissenting opinions as well as orders made in the adjudication of cases.</li>
	<li>Documents containing;	<ol type="i">
		<li>Substantive rules of the institution.</li>
		<li>Statements and interpretations of policy which have been adopted by the institution.</li>
		<li>Final planning policiesm recommendations and decisions.</li>
		<li>Factual reports, inspection reports and studies whether preapared by or for the institution.</li>
		<li>Infromation relating to receipt or expenditure of public or other funds of the institution.</li>
		<li>The names, salaries, titles and dates of employment of all employees and officers of the institution.</li>
		<li>The right of state, public institutions, or of any private person(s)</li>
	</ol></li> 
